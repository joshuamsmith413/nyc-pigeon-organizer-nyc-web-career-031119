def nyc_pigeon_organizer(data)
  # write your code here!
  foo = {}
data.each do |genres, detail|
  detail.each do |key_word, bird_names|
bird_names.each do |name|
if !foo.has_key?(name)
foo[name] = {}
end
if !foo[name].has_key?(genres)
foo[name][genres] = []
end
if !foo[name][genres].include?(key_word)
foo[name][genres] << key_word.to_s
end
end
  end
end
foo
end
